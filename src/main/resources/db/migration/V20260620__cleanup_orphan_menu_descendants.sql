-- 1. 软删 17 个明确无主的孤立老根后代 (前端无组件, 不可达)
--    涵盖 162/163/173/174/175/176/177/178/179/180/181/183/184/189/190/191/192
UPDATE sys_menu
SET is_delete = 1,
    delete_by = 1,
    delete_time = NOW(),
    delete_reason = 'V20260620 孤立老菜单后代清理 (V20260609 漏掉)'
WHERE is_delete = 0
  AND id IN (162, 163, 173, 174, 175, 176, 177, 178, 179, 180, 181, 183, 184, 189, 190, 191, 192);

-- 2. 同步删掉 platform_manager 对这些死菜单的 sys_role_menu 绑定
--    注: sys_role_menu 表无 is_delete 列 (只有 id/role_id/menu_id), 走硬删
--    注: 学员 (role code='student') 不删 — 移动端可能靠这些权限码做接口鉴权
DELETE rm
FROM sys_role_menu rm
JOIN sys_role r ON rm.role_id = r.id
WHERE r.code = 'platform_manager'
  AND rm.menu_id IN (162, 163, 173, 174, 175, 176, 177, 178, 179, 180, 181, 183, 184, 189, 190, 191, 192);
